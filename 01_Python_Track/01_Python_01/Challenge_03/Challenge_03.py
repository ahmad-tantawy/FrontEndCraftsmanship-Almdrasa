# Create a function called "calculate".
# Give it three arguments (three numbers)

def calculate(num1, num2, num3):
    return (num1 + num2) * num3     # Make sure the addition happens first.

# Call the function and print the results
print(calculate(2, 10, 7))
print(calculate(26, 14, 8))
print(calculate(4, -15, 0))
